public class pattren1 {
static void pattren(int n) {
    for(int i=0; i<=n;i++)
    {
        for (int j=i;j<=i;j++) {
            System.out.print("");
    }
}
    System.out.println();
}

    
    public static void main(String[] args){
        int n = 5;
        pattren(n);
    } 
}
       
       